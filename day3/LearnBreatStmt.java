package week1.day3;

public class LearnBreatStmt {

	public static void main(String[] args) {
		// Breaks the loop and comes out of the loop
		//skips the current iteration and executes the remaining
		
		for (int i = 1; i <=10; i++) {
			
			if (i==5) {
				break; // the loop will break the loop when it reaches the i values which is before 5
			}
		System.out.println(i);	
		}
		

	}

}
